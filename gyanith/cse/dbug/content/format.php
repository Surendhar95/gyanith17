
<p><b>The event is conducted in two rounds.</b><br/><br/>
<b>Round 1</b>(tentative round based on the number of participants):<br/>
	Round 1 will be a written test in which the participants are given a list of 15 questions related to programming which they have to answer in 30 min. Depending on the score in round 1, participants will be selected for round 2.<br/><br/>
<b>Round 2:</b>
	In round 2, participants will be given a set of incorrect codes which they have to debug and produce the desired result. The round is sub-divided into three difficulty levels: Easy, Medium and Hard. There will be 15 challenges totally, 5 from each section. 
