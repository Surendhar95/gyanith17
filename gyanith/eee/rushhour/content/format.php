<p><b>The event is conducted in two rounds.</b><br/><br/>
<b>Round 1: </b>
	Simple Line Follower Competition and a simple RF module based test. The path/track is given by the event co-ordinators.<br/><br/>
<b>Round 2: </b>
	This round consists of each SumoBot robot seeking out the opponent robot and pushing it out of the Sumo Ring and to avoid being pushed out of the Sumo Ring itself. The Bots can be RF controlled for this round and need to be the same which were used in the 1st round. This round has two levels. The best two selected from 1st level based on their performance will have a final battle in the 2nd level which is the Final level. 
