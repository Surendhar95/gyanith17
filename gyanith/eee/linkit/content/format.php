<p><b>The event is conducted in two rounds.</b><br/><br/>
<b>Round 1:</b>
	The first round is the puzzle round, where you will be given a technical crossword puzzle which is to be solved within the given time limit. <br/><br/>
<b>Round 2:</b>
	Design the model first based on the problem statement. Upon acceptance of the event organizers, the team can continue with constructing their models. Required components will be provided for the model. Basic idea and hints for the model will be provided within the problem statement.<br/> 

The mini project for example may be designing a counter that counts the number of persons entering and exiting the room thereby giving exact count of people in the room at every instant.<br/>

