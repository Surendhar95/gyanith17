<p><b> The event is conducted in 2 rounds .</b><br/><br/>
<b>Round 1</b>(Prelims)<br/>
	INDENTIFYING THE CIRCUIT:<br/>
Participants will be given a circuitry placed in a covered box, with inputs and outputs floating out. They are supposed to identify the circuit by observing the output(s) for the corresponding input(s).Hints will be provided for identifying the circuit (Whether it’s an analog device, digital device etc.).10 teams which complete the task first will be promoted to  2nd round. <br/>
Time Duration: 1 hr.<br/>
<b>Round 2:</b>
	DIFFUSING THE CIRCUIT:<br/>
Participants will be provided with all the required components or circuits to create an alarm bomb and diffuse it. Team that completes the task first secures the first place.<br/>
Time duration: 1 ½ hr.
