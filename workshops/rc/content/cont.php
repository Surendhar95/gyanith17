<p>
<b>Introduction:</b><br>
The participants will be given a basic introduction about aircrafts, aerodynamics, anatomy and designing.<br><br>
<b>Planning:</b><br>
The full plan and structural design will be given to the participants. They will be made aware about the common mistakes, and failures. <br><br>
<b>Construction:</b><br>
Participants will construct the aircraft under complete guidance of the volunteers and members of the workshop.<br><br> 
<b>Testing and flying:</b><br>
The aircraft made by the participants would be thoroughly inspected for any flaws and ready for a test flight.<br><br>
<b>Certificate distribution:</b><br>
A certificate will be issued under the institute name and distributed to all participants.


