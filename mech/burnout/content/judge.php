<b>Race Event:</b><br/><br/>

 <b>Prelims:</b><br/><ol><li>
All selected teams will now have to compete in a time trial.
</li><br><li>Each team will be given a run of 2 individual laps & the best lap time will be considered.
</li></ol><br/><br/>
  
<b>Free Style:</b><br/><ol><li>
All selected teams will perform a series of tasks (i.e. maneuvering a football on to goal post. etc.) which will be informed half an hour prior to the event.
</li><br><li>Points will be given based on execution of the tasks given.
</li></ol><br/><br/>


 <b>Final Rounds:</b><br/>
This will be a head to head knock out races would be held to determine the Winner.




