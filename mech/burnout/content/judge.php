<b>Inspection:</b><br/><ol><li>
Participants report to the venue with their machines.
</li><br><li>Strict inspection of the machine is carried out according to the guidelines & regulations set by the organizers.
</li><br><li>All selected teams will be given practice slots of 15 minutes each, after the inspection is completed.
