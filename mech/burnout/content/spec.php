<b>Track Specifications:</b><br>
<ol>
<li> The race track will be an off-road dirt track with left & right turns, hairpins, S-shaped curves, bumps and puddles.</li><br>
<li> Participants are advised to use proper suspension systems for their machines to endure the bumps.</li><br>
<li> A lap is deemed to be completed when the machine completes the entire track  & comes back to the starting point.</li><br>
<li> The track will have checkpoints at regular intervals. If a machine tumbles, or halts, or goes off the arena at any point on the track, one of the team members is allowed to lift it up and place it at the nearest checkpoint behind that point. The time shall still be running in the meantime.
</li><br><br></ol>

<b>Technical Specifications:</b>
<b>Engine:</b><br>
<ol>
<li>The maximum allowed capacity of IC engine to be used is 4.6 cc and this will be checked before the competition (i.e. Participants can also use 2.5 cc, 3 cc, 3.5 cc or any other IC engine lower in capacity).
</li><br><li> There is no restriction on the type of IC engine (either glow or diesel cycle engine), but the engine must have throttle control.
</li><br></ol>
 <b>Fuel:</b><br><ol>
<li> The percentage of nitromethane should not exceed 20% by volume in the fuel.
 </li><br></ol><b>Vehicle dimensions:</b><br><ol>
<li> Machine should fit in a box of dimensions 800mm x 600mm x800mm (l x b x h) at any moment of time during the race. The external device which is used to control the machine is not included in the size constraint.
</li><br><li> The machine may be roughly classified into structural and functional parts:<ul>
<li>	Functional parts - Gears, differential gear, engine, springs, shock absorbers, servo motors (non-propulsion purposes only), batteries, wheels, wheel hub are allowed to be used as available in the market.
</li><li>	Structural parts - Chassis, steering mechanism, shock towers, suspension (excluding upper suspension arm, suspension spring and shock absorbers), have to be built by the participants themselves.

</li></ul></li><br><li> The car will be checked for proper throttle control.
</li><br></ol><b>Power supply:</b><br><ol>
<li> The machine must have an on-board power supply to provide power to any mechanism requiring electric power not exceeding 12V.
</li><br></ol><b>Radio controllers:</b><br><ol>
<li> The machine has to be necessarily controlled by a wireless remote control system.
</li><br><li> Participants are required to use a remote control of frequency of band spectrum 2.4 GHz. The above rule is to ensure that there is no frequency clash during the competition and participants are not allowed to use any other remotes with frequency other than 2.4 GHz.(In case of unavailability of remote control(i.e 2.4 GHz) the participants are required to submit their remote in organizers desk and it will returned during respected practise slots and race only, in order to avoid frequency clash)
</li><br></ol><b>Control Space:</b><br><ol>
<li> An elevated podium will be provided for the control of the machine. The podium will ensure good visibility for the controller.
</li><br></ol>
