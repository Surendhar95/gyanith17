<p>  <b>2 Rounds- (Prelims+final)</b><br><br>
<b>ROUND 1:  IDENTIFYING THE CIRCUIT</b><br><br>
Participants will be given a circuitry placed in the covered box, with inputs and outputs floating out. They are supposed to identify the circuit by observing the output(s) for the corresponding input(s).Hints will be provided for identifying the circuit (Whether it’s an analog device, digital device etc.).10 teams which complete the task first will be promoted to 2nd round.
<br><br>Time Duration: 1 hr.<br><br>
<b>ROUND 2:  DIFFUSING THE CIRCUIT</b><br><br>
Participants are provided with all required components or circuits to create an alarm bomb and diffuse it (Coordinators will decide on spot). Team that completes the task first will be the winners.
<br><br>Duration: 1 ½ hr.

