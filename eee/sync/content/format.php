<ol>
<li><b>PRELIMS (Round 1)</b><br/>
Online Test (mostly MCQs) along with spot offline test related to basic electrical and electronic topics.
</li>
<li><b>MAINS (Round 2)</b><br/>
A circuit simulated using multisim is given in a Black box (i.e., components are opaque ). By giving different inputs and validating the corresponding outputs , the circuit components and their functioning is to be identified.
</li>
<li><b>FINALS (Round 3)</b><br/>
Designing a circuit practically based on the problem statement given on spot to the finalists. They need to design the circuit by purchasing the required components with the *virtual money that is earned by them through points from the first two rounds.
<br/>The final judging criteria are done by the event organisers based on the performance.
</li>

</ol>
